int	ft_iterative_factorial(int nb)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	if (nb == 0)
		return (1);
	if (nb > 0 && nb < 13)
	{
		while (i < nb)
			j = j + (j * i++);
		return (j);
	}
	return (0);
}
