int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	if (tab == 0 || f == 0)
		return (0);
	while (tab[i])
	{
		if (f(tab[i++]) == 1)
			count++;
	}
	return (count);
}
